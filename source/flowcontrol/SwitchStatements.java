package source.flowcontrol;

public class SwitchStatements {
    public static void main(String[] args) {
        int x = 900;
        switch (x) {
            case 10:
                System.out.println("10");
                break;
            case 20:
                System.out.println("20");
                break;
            default:
                System.out.println("default");
        }
    }
}
