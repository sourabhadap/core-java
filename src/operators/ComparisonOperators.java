package src.operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        System.out.println(10 == 10);
        System.out.println(10 != 10);
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println('a' < 10);
    }
}
